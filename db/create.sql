-- Add trigger to update Cart_Items price when Products price changes
CREATE OR REPLACE FUNCTION update_cart_items_price()
RETURNS TRIGGER AS $$
BEGIN
    UPDATE Cart_Items
    SET unit_price = NEW.price
    WHERE product_id = NEW.product_id;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER update_cart_items_price_trigger
AFTER UPDATE OF price ON Products
FOR EACH ROW
EXECUTE FUNCTION update_cart_items_price();

-- Add trigger to update Products price when Inventory price changes
CREATE OR REPLACE FUNCTION update_products_price()
RETURNS TRIGGER AS $$
BEGIN
    UPDATE Products
    SET price = NEW.price
    WHERE product_id = NEW.product_id;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER update_products_price_trigger
AFTER UPDATE OF price ON Inventory
FOR EACH ROW
EXECUTE FUNCTION update_products_price();
